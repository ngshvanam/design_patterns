// Facade pattern allows us to take complex subsystem and make it easier to use by implementing Facade calss that provides one or more reasonable interfaces.
// Face and adapter are different with their intent.
// The intent of Adapter pattern is to alter the interface so that it matches with what client expecting.
// The intent of Facade pattern is the provide the simplest interface for complex sub system.
// An adapter wraps object to change it's interface.
// An Facade wraps set of objects to simplify.
// An Decorator wraps object to add behaviours dynamically.
// Design principles
// Principle of least knowledge: Talk only to your immediate friends.
